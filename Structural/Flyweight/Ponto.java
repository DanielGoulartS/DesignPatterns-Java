package Design_Patterns.Structural.Flyweight;

/**
 *
 * @author Daniel
 */
public class Ponto {
    int x;
    int y;
    
    public Ponto(int x,int y){
        
        this.x = x;
        this.y = y;
        
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    
}
